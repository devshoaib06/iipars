    <p>The components of <strong>Dissertation</strong>  are research methodology and training both qualitative and quantitative, statistics, applied research methods, basic computing and scientific communications, management principles, social; ethics, writing reviews, presentations and seminars including preparation for Viva Voce, review of literature, referencing style, formulation of research questions, questionnaire development and other areas relevant to research.<br>
It is difficult to give a structure that should be followed universally. However, most dissertations should have a structure similar to the following.
</p>
    <br>
    <br>

   <p> <strong>Dissertation Structure:</strong><br><br></p>

<p><strong>Abstract:</strong> This is usually 200-250 words and describes the subject of the research, the methodology employed and the main findings and recommendation.</p><br>

<p><strong>Introduction:</strong>  A rationale and justification for the need for the study. A broad statement of the purpose of the study and its aims and objectives. The research problem, specifics and justification of these objectives should be included in this section.</p><br>

<p><strong>Literature Review:</strong>  The literature review considers the work of theorists in the area and compares their work with others. The section should not merely be descriptive, but rather should compare and contrast various perspectives on the matter. All relevant and recent work on the topic should be reviewed. In essence the literature review is a thorough and selective appraisal of the topic. It should discuss the key theories and concepts related to the research aims and objectives.</p><br>

<p><strong>Methodology:</strong> This section is used to provide details of the design of the research. There is normally a reiteration of the objectives. It should include a rationale for the choice of research design/approach, the procedure used, details of the empirical work and the method of analysis.</p><br>

<p><strong>Findings:</strong> Analysis of results/data.</p><br>

<p><strong>Discussion:</strong> This section is probably the most important section of the dissertation and the findings of the work should be discussed in detail in relation to the original issues/problem. An interpretation of the findings and a discussion of their practical and theoretical implications should be included.</p><br>

<p><strong>Conclusions & Recommendations:</strong> The work should be concluded by identifying and highlighting the key findings and by suggesting some areas for improvement and/or further work depending on the topic.</p><br>

<p><strong>Appendices:</strong> Material which is relevant to the work, but which is too long, detailed or in some form that would interrupt the flow of the work is inserted in the appendices. Examples of such material would be the questionnaires and computer programmes.</p><br>

<p><strong>References:</strong> The reference section must include all the references that are cited in the dissertation, these should be presented using the Harvard/APA system (please see point 6 on page eight for further details).</p><br>

<p><strong>Bibliography:</strong> A list (Harvard/APA system) of the books and articles consulted within the dissertation but not cited in your text.</p><br>

    
 


