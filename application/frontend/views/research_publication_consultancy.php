  <h2 class="mt-0 line-height-1">Research Paper<span class="text-theme-colored2"> Publication</span></h2>
  
   <p>
    <strong>Importance of Research and Journal Publications in Higher Studies</strong>
    </p>
    <br>
    <p>In the rat race to emerge as a rank holder in the class, the essence of student life has lost its charm. At the end of a semester, what the world awaits for is the report cards and the marks imprinted over them. Quite adjusted and groomed to entertain such a mindset, students these days are mugging up the lessons rather than going in-depth into the topic. They are no longer interested in going any further beyond the academic syllabus. This mindset continues in higher studies as well, as students feel more compelled to land in a job right after the completion of graduation. Very few of them acknowledge the importance of research publications in academic careers. Nowadays, only a handful of students are interested in pursuing their career in research as it demands dedication and effort.</p><br>

    <p>
    <strong>Benefits of Publishing a Research Paper</strong>
    </p>
    <br>
    <p>There is great relevance to research in education. Apart from ensuring an in-depth knowledge over a topic, scientific and historical research papers also contribute to the world of knowledge. These journals or papers become the helping hands for the students in future guiding and helping them in research. Students need to understand the role of scientific research in society and the importance of scientific research in education as it will ultimately reward them in return.<br>
Some of the key points that highlight the importance of publishing research papers in UG / PG are mentioned below:
</p>
<br>
<p>
  1.  <strong>Enhancement of Teaching Practices:</strong> Teaching methodologies will certainly develop if the teachers tend to do more research and analysis over the topic. This is referred to as research- oriented teaching, where the educators are engaged in acquiring knowledge and information outside the textbook, to get modern and innovative information over a subject for the students.<br><br>

2.  <strong>Students Tend to Learn Effectively:</strong> Students tend to learn more proficiently when they learn through research activities. Research-based learning supports the genuine interest of a student while learning about a particular topic. Guided by seniors and professors, students get clarity of concept about a particular subject.<br><br>

3.  <strong>Encouragement of Research-Based Practice and Knowledge Production:</strong> Promoting motivation and encouragement among the students to advance further regarding the topic and publish their own journals, implementation of research activities in studies can be of high
significance. The educational institutes must understand the significance of research and design their curriculum accordingly by assigning some professors to work solely for research and journal publications.<br><br>

4.  <strong>A Chance of Getting Admission into Top Colleges:</strong> Most of the universities will offer admission to the students if their research paper gets published in reputed scientific journals. The students, who have proficient research papers available in their names, possess a maximum chance to get a scholarship to study in the top universities of the world. Apart from that, students can also apply to different courses and scholarships worldwide seeking admission based on their research works.<br><br>

5.  <strong>Helps in Building a Professional Career:</strong> Considered as one of the requirements for Master’s and Doctorate students, journal paper adds the glory in the professional careers of the students. With more acquired knowledge through research methodologies, a person can share knowledge with the world by teaching or assisting fellow students in research activities and journal publications.

</p><br><br>

<p>
  <h2>A. No of Papers Required:</h2>
</p><br>

<p>* For would be professors:&nbsp;&nbsp;&nbsp;&nbsp;At least 6 papers<br><br>
* For existing professors:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp&nbspAt least 6 papers (1+3+2)<br><br>
* For PTT/GT/SACT:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp&nbsp;&nbsp;&nbsp;&nbsp&nbspAt least 4 papers<br><br>
</p><br>

<p>
  <h2>B. Category of Journal:</h2>
</p><br>

<p><strong>1. UGC-CARE (Category-I)</strong><br>
The University Grant Commission has established “Consortium for Academic and Research Ethics” (CARE) for creation and maintenance of “Reference List of Quality Journals”. CARE members include Statutory Councils/Academies/ Government bodies and others like the Association of Indian Universities.
</p><br><br>


<p><strong>2. UGC-CARE (Category-II): Elsevier's abstract and citation database</strong><br>
Scopus is Elsevier's abstract and citation database launched in 2004. All journals covered in the Scopus database, regardless of who they are published under, are reviewed each year to ensure high quality standards are maintained. Searches in Scopus also incorporate searches of patent databases.
</p><br><br>

<p><strong>3. UGC-CARE (Category-II): Web of Science</strong><br>
Web of Science (WOS) was the only international and multidisciplinary database until 2004. Elsevier introduced Scopus in 2004 as a good replacement for WOS. These two databases to index prestigious journals have been compared from different aspects.
</p><br><br>

<p><strong>4. UGC Approved journal</strong><br>
The UGC approved List of Journals contain 38,653 journals that are indexed in Web of Science, Scopus and Indian Citation Index. Besides, journals covered in a selected indexing and abstracting services are also being added to the UGC Approved List of Journals.</p><br><br>
 


<p><strong>5. Peer-reviewed Journal</strong><br>
Peer-reviewed (refereed or scholarly) journals - Articles are written by experts and are reviewed by several other experts in the field before the article is published in the journal in order to ensure the article's quality. (The article is more likely to be scientifically valid, reach reasonable conclusions, etc.)<br><br>
Scopus indexing journal is much desirable than other categories, but due to some factors like (publication needs much time / it maintains very high standard / it is expensive for publication) – we often opt for CARE listed / approved journal.
</p><br><br>

